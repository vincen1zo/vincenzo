package vo;

public class interiorReviewVo {
	private String name;
	private String profileImgUrl;
	
	public interiorReviewVo(String name, String profileImgUrl) {
		this.name = name;
		this.profileImgUrl = profileImgUrl;
	}
	public String getName() {
		return name;
	}
	public String getProfileImgUrl() {
		return profileImgUrl;
	}
	
	
}
